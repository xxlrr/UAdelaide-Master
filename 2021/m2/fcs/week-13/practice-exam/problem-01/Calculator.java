
public class Calculator {

    public int sum(int numA, int numB){
        return numA + numB;
    }

    public int sub(int numA, int numB){
        return numA - numB;
    }

    public float multiply(float numA, float numB){
        return numA * numB;
    }

    public float division(float numA, float numB){
        return numA / numB;
    }
}
