public class Calculator {
    
    public int addn(int numA, int numB) {
        return numA + numB;
    }

    public int sub(int numA, int numB) {
        return numA - numB;
    }

    public int multiply(int numA, int numB) {
        return numA * numB;
    }

    public float division(float numA, float numB) {
        return numA / numB;
    }
    
}
